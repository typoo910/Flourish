namespace AckSS.Flourish.Abstract;

/// <summary>
/// Describes the requested Flourish application theme.
/// </summary>
public enum FlourishTheme
{
    /// <summary>
    /// Follow the current Windows app theme.
    /// </summary>
    System,

    /// <summary>
    /// Use the light Flourish resources.
    /// </summary>
    Light,

    /// <summary>
    /// Use the dark Flourish resources.
    /// </summary>
    Dark,
}
